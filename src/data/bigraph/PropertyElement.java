package data.bigraph;

public class PropertyElement {
	private String propertyName;
	private String upperBound;
	private String lowerBound;
	private String value;
	private String valueType;
	
	public PropertyElement(String proName, String upper, String lower, String value, String valueType){
		this.propertyName = proName;
		this.upperBound = upper;
		this.lowerBound = lower;
		this.value = value;
		this.valueType = valueType;
	}
	
	public String getPropertyName() {
		return propertyName;
	}
	
	public void setPropertyName(String propertyName) {
		this.propertyName = propertyName;
	}
	
	public String getUpperBound() {
		return upperBound;
	}
	
	public void setUpperBound(String upperBound) {
		this.upperBound = upperBound;
	}
	
	public String getLowerBound() {
		return lowerBound;
	}
	
	public void setLowerBound(String lowerBound) {
		this.lowerBound = lowerBound;
	}
	
	public String getValue() {
		return value;
	}
	
	public void setValue(String value) {
		this.value = value;
	}
	
	public String getValueType() {
		return valueType;
	}

	public void setValueType(String valueType) {
		this.valueType = valueType;
	}

	public void printPropertyElement(){
		System.out.println("Property Name: " + this.propertyName);
		System.out.println("Value: " + this.value);
		System.out.println("Value Type: " + this.valueType);
		System.out.println("Upper Bound: " + this.upperBound);
		System.out.println("Lower Bound: " + this.lowerBound);
	}
}
